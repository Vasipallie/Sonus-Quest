void clearSongList() {
  songCount = 0;
  for (int i = 0; i < MAX_SONGS; i++) {
    Index[i] = "";
    Title[i] = "";
    Artist[i] = "";
    Filename[i] = "";
    Duration[i] = "";
  }
}

void parseLine(String line, int i) {
  int dash = line.indexOf('-');
  int eq = line.indexOf('=');
  int plus = line.indexOf('+');
  int gt = line.indexOf('>');

  if (dash < 0 || eq < 0 || plus < 0 || gt < 0) return;

  Index[i] = line.substring(0, dash);
  Title[i] = line.substring(dash + 1, eq);
  Artist[i] = line.substring(eq + 1, plus);
  Filename[i] = line.substring(plus + 1, gt);
  Duration[i] = line.substring(gt + 1);
}

void get_songlist(int songpos) {
  clearSongList();

  File file = SD.open("/sdir.txt");
  if (!file) {
    scerror("SDir Not Found");
    return;
  }

  int current = -1;
  while (file.available() && songCount < MAX_SONGS) {
    String line = file.readStringUntil(';');
    line.trim();
    if (line.length() == 0) continue;

    current++;
    if (current < songpos) continue;

    parseLine(line, songCount);
    if (Title[songCount].length() > 0) {
      songCount++;
    }
  }

  file.close();

  selectedSong = 0;
  visibleTop = 0;
}

void saveSpeaker(const String& name, const esp_bd_addr_t addr) {
  SD.remove("/data.txt");
  File f = SD.open("/data.txt", FILE_WRITE);
  if (!f) {
    Serial.println("[SD] Could not open data.txt for writing");
    return;
  }
  f.print("SPEAKER:");
  f.println(name);
  char addrBuf[18];
  snprintf(addrBuf, sizeof(addrBuf), "%02x:%02x:%02x:%02x:%02x:%02x",
           addr[0], addr[1], addr[2], addr[3], addr[4], addr[5]);
  f.print("ADDR:");
  f.println(addrBuf);
  f.close();
  Serial.printf("[SD] Saved speaker: %s  addr: %s\n", name.c_str(), addrBuf);
}

