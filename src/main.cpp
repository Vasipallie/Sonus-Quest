static bool init_sd(uint8_t cs_pin, uint8_t max_retries = 5) {
    for (uint8_t attempt = 1; attempt <= max_retries; ++attempt) {
        Serial.printf("[SD] Init attempt %d/%d (CS=GPIO%d)...\n",
                      attempt, max_retries, cs_pin);
        if (SD.begin(cs_pin)) {
            uint8_t card_type = SD.cardType();
            const char* type_str = "UNKNOWN";
            if      (card_type == CARD_MMC)  type_str = "MMC";
            else if (card_type == CARD_SD)   type_str = "SDSC";
            else if (card_type == CARD_SDHC) type_str = "SDHC";
            else if (card_type == CARD_NONE) type_str = "NONE (no card?)";

            Serial.printf("[SD] Card type : %s\n", type_str);
            Serial.printf("[SD] Card size : %llu MB\n",
                          SD.cardSize() / (1024ULL * 1024ULL));
            Serial.printf("[SD] Total space: %llu MB used / %llu MB total\n",
                          SD.usedBytes() / (1024ULL * 1024ULL),
                          SD.totalBytes() / (1024ULL * 1024ULL));
            return true;
        }
        Serial.printf("[SD] Mount failed on attempt %d. Retrying in 1 s...\n", attempt);
        delay(1000);
    }
    return false;
}
