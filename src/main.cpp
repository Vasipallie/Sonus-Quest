void openAudioFile(int songIndex) {
  if (songIndex < 0 || songIndex >= songCount) return;
  if (Filename[songIndex].length() == 0) return;

  audio_sd_ready = false;
  if (audio_file) audio_file.close();

  String path = "/" + Filename[songIndex];
  audio_file = SD.open(path, FILE_READ);
  if (!audio_file) {
    Serial.printf("[BT]  Cannot open audio file: %s\n", path.c_str());
    scerror("Audio file 404");
    audio_sd_ready = false;
  } else {
    Serial.printf("[BT]  Opened: %s  (%llu bytes)\n",
                  path.c_str(), (unsigned long long)audio_file.size());
    audio_sd_ready   = true;
    audio_playing_index = songIndex;
    screenname = "PLAYING";
    musicplay(Title[songIndex], Artist[songIndex], Duration[songIndex]);

  }
}
int32_t get_sound_data(uint8_t* data, int32_t byte_count) {
  if (!audio_sd_ready || !audio_file) {
    memset(data, 0, byte_count);
    return byte_count;
  }

  int32_t bytes_read = (int32_t)audio_file.read(data, (size_t)byte_count);
  if (bytes_read == 0) {
    if (!audio_switch_pending) {
      int next_index = audio_playing_index + 1;
      if (next_index >= songCount) {
        next_index = 0;
      }
      audio_pending_index = next_index;
      audio_switch_pending = true;
    }
    memset(data, 0, byte_count);
    return byte_count;
  }

  if (bytes_read > 0) {
    int16_t* samples = (int16_t*)data;
    int32_t sample_count = bytes_read / sizeof(int16_t);
    float gain = volume_gain;
    for (int32_t i = 0; i < sample_count; i++) {
      int32_t val = (int32_t)(samples[i] * gain);
      if (val > 32767) val = 32767;
      else if (val < -32768) val = -32768;
      samples[i] = (int16_t)val;
    }
  }

  if (bytes_read < byte_count) { 
    memset(data + bytes_read, 0, byte_count - bytes_read);
    bytes_read = byte_count;
  }
  return bytes_read;
}
bool on_device_discovered(const char* name, esp_bd_addr_t addr, int rssi) {
  if (TARGET_SPEAKER == "NULL" || TARGET_SPEAKER.length() == 0) return false;

  if (name && strlen(name) > 0) {
    Serial.printf("[BT]  Discovered: %-30s  RSSI=%d dBm\n", name, rssi);
  }
  if (name && String(name) == TARGET_SPEAKER) {
    Serial.printf("[BT]  *** Target '%s' matched — connecting ***\n",
                  TARGET_SPEAKER.c_str());
    static const esp_bd_addr_t zeroAddr = {0};
    if (memcmp(addr, TARGET_ADDR, sizeof(esp_bd_addr_t)) != 0 &&
        memcmp(addr, zeroAddr,    sizeof(esp_bd_addr_t)) != 0) {
      memcpy(TARGET_ADDR, addr, sizeof(esp_bd_addr_t));
      pending_addr_save = true;  
    }
    return true;
  }
  return false;  
}
void on_connection_state_changed(esp_a2d_connection_state_t state, void*) {
  switch (state) {
    case ESP_A2D_CONNECTION_STATE_DISCONNECTED:
      Serial.printf("[BT]  Disconnected — retrying in %d s\n", RECONNECT_INTERVAL);
      bt_connected      = false;
      bt_status_changed = true;
      break;
    case ESP_A2D_CONNECTION_STATE_CONNECTING:
      Serial.printf("[BT]  Connecting to '%s'...\n", TARGET_SPEAKER.c_str());
      break;
    case ESP_A2D_CONNECTION_STATE_CONNECTED:
      Serial.printf("[BT]  Connected to '%s' — streaming\n", TARGET_SPEAKER.c_str());
      bt_connected      = true;
      bt_status_changed = true;
      break;
    case ESP_A2D_CONNECTION_STATE_DISCONNECTING:
      Serial.println("[BT]  Disconnecting...");
      break;
    default:
      Serial.printf("[BT]  State: %d\n", (int)state);
      break;
  }
}

void setup(){
    Serial.begin(115200);
    delay(500);
    update_volume_gain();
    pinMode(BTN_NEXT, INPUT_PULLUP);
    pinMode(BTN_BACK, INPUT_PULLUP);
    pinMode(CONFIRM, INPUT_PULLUP);/* 
    pinMode(RIGHT, INPUT_PULLUP);
    pinMode(LEFT, INPUT_PULLUP); */

    pinMode(UP, INPUT_PULLUP);
    pinMode(DOWN, INPUT_PULLUP);

    pinMode(TFT_BL, OUTPUT);
    digitalWrite(TFT_BL, HIGH);
    pinMode(SD_CS_PIN, OUTPUT);
    digitalWrite(SD_CS_PIN, HIGH);

    tft.initR(INITR_BLACKTAB);
    tft.setRotation(0);
    tft.setTextWrap(false);
    tft.fillScreen(ST77XX_BLACK);

    boot();
    if (!init_sd(SD_CS_PIN)) {
        Serial.println("[SD] ERROR! Init failed");
        scerror("SD Card Failed");
        while (true) delay(1000);
    }
    File file = SD.open("/data.txt");
    if (!file) {
        TARGET_SPEAKER = "NULL";
        Serial.println("[BT] No data.txt found, using NULL speaker");
    } else {
        while (file.available()) {
            String line = file.readStringUntil('\n');
            line.trim();
            if (line.startsWith("SPEAKER:")) {
                TARGET_SPEAKER = line.substring(8);
                TARGET_SPEAKER.trim();
            } else if (line.startsWith("ADDR:")) {
                String addrStr = line.substring(5);
                addrStr.trim();
                sscanf(addrStr.c_str(), "%hhx:%hhx:%hhx:%hhx:%hhx:%hhx",
                       &TARGET_ADDR[0], &TARGET_ADDR[1], &TARGET_ADDR[2],
                       &TARGET_ADDR[3], &TARGET_ADDR[4], &TARGET_ADDR[5]);
                Serial.printf("[BT] Loaded MAC: %s\n", addrStr.c_str());
            }
        }
        file.close();
    }
    Serial.printf("[BT] Speaker: %s\n", TARGET_SPEAKER.c_str());

    homem();
    get_songlist(0);
    screenname = "HOMEM";

    Serial.println("[BT] Registering BT callbacks");
    a2dp_source.set_auto_reconnect(false);
    a2dp_source.set_on_connection_state_changed(on_connection_state_changed);
    a2dp_source.set_ssid_callback(on_device_discovered);
    a2dp_source.set_data_callback(get_sound_data);
    if (TARGET_SPEAKER != "NULL" && TARGET_SPEAKER.length() > 0) {
        Serial.printf("[BT] Auto-connecting to saved speaker: %s\n", TARGET_SPEAKER.c_str());
        static const esp_bd_addr_t zeroAddr = {0};
        if (memcmp(TARGET_ADDR, zeroAddr, sizeof(esp_bd_addr_t)) != 0) {
            Serial.println("[BT] Using MAC address for fast auto-reconnect");
            a2dp_source.set_auto_reconnect(TARGET_ADDR, 5); // 5 retries before fallback to scanning
        } else {
            a2dp_source.set_auto_reconnect(true, 5);
        }
        static char speakerBuf[64];
        TARGET_SPEAKER.toCharArray(speakerBuf, sizeof(speakerBuf));
        a2dp_source.start(speakerBuf);
    }
}
