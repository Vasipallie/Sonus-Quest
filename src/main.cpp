void update_volume_gain() {
  if (current_volume <= MIN_VOLUME) {
    volume_gain = 0.0f;
  } else if (current_volume >= MAX_VOLUME) {
    volume_gain = 1.0f;
  } else {
    float db = -30.0f + 30.0f * (float)(current_volume - 1) / (float)(MAX_VOLUME - 1);
    volume_gain = pow(10.0f, db / 20.0f);
  }
  Serial.printf("[VOLUME] Level: %d, Gain: %.4f\n", current_volume, volume_gain);
}
